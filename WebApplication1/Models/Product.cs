using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.ComponentModel.DataAnnotations;
using System.ComponentModel.DataAnnotations.Schema;

namespace TechmerVision.Models
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   A product. </summary>
    ///
    /// <remarks>   Aedmonds, 8/25/2017. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class Product : Asset
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier of the product template. </summary>
        ///
        /// <value> The identifier of the product template. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public long ProductTemplateId { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the title. </summary>
        ///
        /// <value> The title. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public String Title { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        ///     Gets or sets a value indicating whether this object has background image.
        /// </summary>
        ///
        /// <value> True if this object has background image, false if not. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public Boolean HasBackgroundImage { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the background image. </summary>
        ///
        /// <value> The background image. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public String BackgroundImage { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier of the parent workspace. </summary>
        ///
        /// <value> The identifier of the workspace. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public long WorkspaceId { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Accessor Property for the parent workspace. </summary>
        ///
        /// <value> The workspace. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [ForeignKey("WorkspaceId")]
        public Workspace Workspace { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Accessor property for the linked ProductTemplate. </summary>
        ///
        /// <value> The product template. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [ForeignKey("ProductTemplateId")]
        public virtual ProductTemplate ProductTemplate { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Accessor Method for the ProductColors Child Element. </summary>
        ///
        /// <value> A list of colors of the products. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public virtual ICollection<ProductColor> ProductColors { get; set; }
    }
}